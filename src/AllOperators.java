public class AllOperators {

    public static void main(String[] args){

        int a=10, b=30;

        //Sum of two numbers
        a+=b;

        System.out.println("Sum of a and b is  " +a);

        //subtraction of two numbers
        a-=b;
        System.out.println("Subtract of a(40) - b(30) is " +a);

        //Multiplication of two numbers
        a*=b;
        System.out.println("Multiplication of a(10) * b(30) is " +a);

        //Division of two numbers is
        a/=b;
        System.out.println("Division of two a(300) by b(30) is " +a);

        a=++b;
        System.out.println("Increment is  " +a);

        a=--b;
        System.out.println("Decreement is  "+a);


        int p=20, q=10;

        System.out.println((p==q) +"\t");




    }

}

